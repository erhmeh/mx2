extern Port PortA, PortB, PortC, PortD, PortE, PortF, PortG;
////////////////////////////////////////////////////////////////////////////////
////////////////////////////////////////////////////////////////////////////////
////////////////////////////////////////////////////////////////////////////////
////////////////////////////////////////////////////////////////////////////////
////////////////////////////////////////////////////////////////////////////////
void LED_Init(eLEDS pLED)
{
    switch (pLED)
    {
	case eLED1:
	    Port_SetPinType(&PortC, LED1, eTypeOutput);
	    break;
	case eLED2:
	    Port_SetPinType(&PortC, LED2, eTypeOutput);
	    break;
    }
    LED_Set(pLED, OFF);
}
////////////////////////////////////////////////////////////////////////////////
////////////////////////////////////////////////////////////////////////////////
////////////////////////////////////////////////////////////////////////////////
////////////////////////////////////////////////////////////////////////////////
////////////////////////////////////////////////////////////////////////////////
void LED_Set(eLEDS pLED, UINT8 pValue)
{
    switch (pLED)
    {
	case eLED1:
	    Port_SetPin(&PortC, LED1, pValue);
	    break;
	case eLED2:
	    Port_SetPin(&PortC, LED2, pValue);
	    break;
    }
}
////////////////////////////////////////////////////////////////////////////////
////////////////////////////////////////////////////////////////////////////////
////////////////////////////////////////////////////////////////////////////////
////////////////////////////////////////////////////////////////////////////////
////////////////////////////////////////////////////////////////////////////////
void LED_Toggle(eLEDS pLED)
{
    switch (pLED)
    {
	case eLED1:
	    Port_TogglePin(&PortC, LED1);
	    break;
	case eLED2:
	    Port_TogglePin(&PortC, LED2);
	    break;
    }
}
